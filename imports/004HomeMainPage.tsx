function EmergencyBlock() {
  /**
   * Обработчик кликов по карточкам экстренной помощи
   * Выполняет логику при нажатии на различные карточки помощи
   * Карточки поддерживают как клики, так и drag/swipe навигацию
   * @param cardId - идентификатор нажатой карточки
   */
  const handleEmergencyCardClick = (cardId: string) => {
    console.log(`Emergency card clicked: ${cardId}`);
    
    // Логика обработки различных типов карточек экстренной помощи
    switch (cardId) {
      case 'sensory':
        console.log('Opening sensory grounding technique');
        alert('Техника сенсорного заземления:\n\n5 вещей, которые вы видите\n4 вещи, которые слышите\n3 вещи, которых касаетесь\n2 запаха\n1 вкус');
        break;
      case 'muscle':
        console.log('Opening progressive muscle relaxation');
        alert('Прогрессивная мышечная релаксация:\n\nНапрягите и расслабьте каждую группу мышц, начиная с пальцев ног и поднимаясь вверх');
        break;
      case 'bursts':
        console.log('Opening intense short bursts technique');
        alert('Интенсивные короткие всплески:\n\n30 секунд интенсивных физических упражнений для быстрого снятия стресса');
        break;
      case 'diving':
        console.log('Opening cold diving reflex technique');
        alert('Рефлекс холодного погружения:\n\nОпустите лицо в холодную воду или подержите холодный предмет у висков');
        break;
      case 'breathing':
        console.log('Opening emergency breathing patterns');
        alert('Экстренные дыхательные упражнения:\n\nВдох на 4 счета, задержка на 7, выдох на 8. Повторите 4 раза');
        break;
      default:
        console.log('Unknown emergency card type');
        alert('Выбрана техника экстренной помощи');
    }
  };

  return (
    <div
      className="box-border content-stretch flex flex-col gap-5 items-start justify-start p-0 relative shrink-0"
      data-name="Emergency_block"
    >
      {/* Заголовок блока экстренной помощи */}
      <div className="font-['Kreon:Regular',_sans-serif] font-normal leading-[0] relative shrink-0 text-[#e1ff00] text-[24px] text-left w-[351px]">
        <p className="block leading-[0.8]">Quick mental help</p>
      </div>
      {/* 
       * Горизонтальный слайдер с карточками экстренной помощи
       * Поддерживает:
       * - Клики по карточкам для их активации
       * - Drag/swipe навигацию для прокрутки
       * - Touch события для мобильных устройств
       * - Mouse события для десктопа
       */}
      <EmergencySlider onCardClick={handleEmergencyCardClick} />
    </div>
  );
}

